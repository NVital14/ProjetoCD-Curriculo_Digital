    private void btnPersonCVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPersonCVActionPerformed
        try {
            textAreaCVPerson.setText("");
            String s = curriculo.loadPersonEvents(txtNameCV.getText(), false);
            if (!"".equals(s)) {
                textAreaCVPerson.setText(s);
            } else if("".equals(txtNameCV.getText())) {
                JOptionPane.showMessageDialog(this, "Coloca o nome da pessoa que queres procurar.", "Coloca o nome!", JOptionPane.ERROR_MESSAGE);
            } else {
                // Mostra uma mensagem de erro no caso de n√£o haver essa pessoa
                JOptionPane.showMessageDialog(this, "N√£o existem curr√≠culos dessa pessoa.", "N√£o existe!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPersonCVActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        btnAdd.setEnabled(false);
        new Thread(() -> {
            try {
                // Verifica se o utilizador √© uma institui√ß√£o
                if (myUser.isInstitute()) {
                    // Cria a submiss√£o normalmente se o utilizador for uma institui√ß√£o
                    Submission s = new Submission(
                        myUser,
                        txtName.getText(),
                        txtEvent.getText()
                    );

                    // Adiciona a submiss√£o ao curr√≠culo e atualiza o campo de texto
                    curriculo.add(s);
                    elements.add(s);
                    SwingUtilities.invokeLater(() -> {
                        txtCV.setText(curriculo.submissions.toString());
                        btnAdd.setEnabled(true);
                    });
                    curriculo.save(fileCurriculo, false);
                } else {
                    // Mostra uma mensagem de erro caso o utilizador n√£o seja uma institui√ß√£o
                    JOptionPane.showMessageDialog(this, "Apenas Institui√ß√µes podem adicionar submiss√µes.", "Acesso Negado", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }).start();
    }//GEN-LAST:event_btnAddActionPerformed

